	GPU_POWER_CHECK DISK_PREFLIGHT_MIN_GB PRE_LAUNCH_CMD POST_LAUNCH_CMD NVIDIA_POWER_MODE
	DISABLE_CPU_AFFINITY GAME_PERFORMANCE
)

# known_config_key — Return 0 if key is a recognized launch.d setting.
known_config_key() {
	local key=$1 k
	[[ "$key" == INCLUDE ]] && return 0
	for k in "${LAUNCHLAYER_CONFIG_KEYS[@]}"; do
		[[ "$key" == "$k" ]] && return 0
	done
	case "$key" in
		PROTON_*|DXVK_*|VKD3D_*|__GL_*|__VK_*|WINE*|STEAM_*) return 0 ;;
	esac
	return 1
}
