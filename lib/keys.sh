# Subset shown in --show-config summaries and JSON settings output.
LAUNCHLAYER_SUMMARY_KEYS=(
	INCLUDE BENCHMARK GAMEMODE MANGOHUD GAMESCOPE VRAM_HOGS NETWORK_TUNE PIPEWIRE_LOW_LATENCY
	LAUNCH_WRAPPERS LAUNCH_WRAPPERS_BEFORE GAME_EXTRA_ARGS UNSET_VARS FORCE_NATIVE FORCE_PROTON
